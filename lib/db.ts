export async function toggleForumLike(postId: string, userId: string) {
  const existing = await prisma.forumLike.findUnique({
    where: { postId_userId: { postId, userId } },
  })

  if (existing) {
    await prisma.$transaction([
      prisma.forumLike.delete({ where: { postId_userId: { postId, userId } } }),
      prisma.forumPost.update({
        where: { id: postId },
        data: { likesCount: { decrement: 1 } },
      }),
    ])
    return { liked: false }
  }

  await prisma.$transaction([
    prisma.forumLike.create({ data: { postId, userId } }),
    prisma.forumPost.update({
      where: { id: postId },
      data: { likesCount: { increment: 1 } },
    }),
  ])
  return { liked: true }
}

export async function createForumReply(postId: string, userId: string, content: string) {
  const reply = await prisma.forumReply.create({
    data: { postId, userId, content },
    include: { user: true },
  })

  await prisma.forumPost.update({
    where: { id: postId },
    data: { replyCount: { increment: 1 } },
  })

  return reply
}

export async function getForumPost(postId: string, currentUserId?: string) {
  const post = await prisma.forumPost.findUnique({
    where: { id: postId },
    include: {
      user: true,
      replies: {
        orderBy: { createdAt: 'asc' },
        include: { user: true },
      },
      likes: currentUserId
        ? {
            where: { userId: currentUserId },
            select: { id: true },
          }
        : false,
    },
  })

  if (!post) return null

  return {
    ...post,
    likedByMe: Array.isArray(post.likes) ? post.likes.length > 0 : false,
  }
}
