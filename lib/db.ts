export async function getUserByAuthAccount(provider: string, providerAccountId: string) {
  const account = await prisma.authAccount.findUnique({
    where: { provider_providerAccountId: { provider, providerAccountId } },
    include: { user: true },
  })
  return account?.user ?? null
}

export async function linkAuthAccountToUser(input: {
  userId: string
  provider: string
  providerAccountId: string
  email?: string | null
}) {
  return prisma.authAccount.upsert({
    where: {
      provider_providerAccountId: {
        provider: input.provider,
        providerAccountId: input.providerAccountId,
      },
    },
    update: {
      userId: input.userId,
      email: input.email ?? undefined,
    },
    create: {
      userId: input.userId,
      provider: input.provider,
      providerAccountId: input.providerAccountId,
      email: input.email ?? undefined,
    },
  })
}

export async function resolveOAuthUser(input: {
  provider: string
  providerAccountId: string
  email?: string | null
  name?: string | null
  image?: string | null
}) {
  const linked = await getUserByAuthAccount(input.provider, input.providerAccountId)
  if (linked) {
    return prisma.user.update({
      where: { id: linked.id },
      data: {
        email: linked.email ?? input.email ?? undefined,
        name: input.name ?? undefined,
        image: input.image ?? undefined,
        provider: input.provider,
        lastLogin: new Date(),
      },
    })
  }

  const normalizedEmail = input.email?.toLowerCase().trim() ?? null
  const existingByEmail = normalizedEmail ? await getUserByEmail(normalizedEmail) : null
  const user =
    existingByEmail ??
    (await upsertUser({
      email: normalizedEmail,
      name: input.name,
      image: input.image,
      provider: input.provider,
    }))

  await linkAuthAccountToUser({
    userId: user.id,
    provider: input.provider,
    providerAccountId: input.providerAccountId,
    email: normalizedEmail,
  })

  return user
}

