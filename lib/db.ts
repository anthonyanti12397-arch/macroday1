export async function syncUserLocalData(userId: string, payload: MigrationPayload) {
  const updateUser: Prisma.UserUpdateInput = {}
  const profile = payload.profile

  if (profile) {
    updateUser.preferredCuisine = profile.preferredCuisine ?? null
    updateUser.dietaryRestrictions = asJson(profile.dietaryRestrictions ?? [])
    updateUser.dislikedIngredients = asJson(profile.dislikedIngredients ?? [])
    if (typeof profile.isPro === 'boolean') {
      updateUser.isPro = profile.isPro
    }
    if (typeof profile.isAdFree === 'boolean') {
      updateUser.isAdFree = profile.isAdFree
    }
    // New: Sync autoGenerateDaily if present
    if (typeof (profile as any).autoGenerateDaily === 'boolean') {
      updateUser.autoGenerateDaily = (profile as any).autoGenerateDaily
    }
    // New: Sync goal
    if (profile.goal) {
      updateUser.goal = profile.goal
    }

    updateUser.streak = payload.currentStreak ?? 0
  }

  if (payload.appState) {
    updateUser.localState = asJson(payload.appState)
  }

  if (Object.keys(updateUser).length > 0) {
    await prisma.user.update({
      where: { id: userId },
      data: updateUser,
    })
  }

  if (payload.inbodyHistory.length > 0) {
    await prisma.inBodyEntry.deleteMany({ where: { userId } })
    await prisma.inBodyEntry.createMany({
      data: payload.inbodyHistory.map((record) => ({
        userId,
        entryDate: record.date,
        weight: record.weight,
        height: record.height,
        gender: record.gender,
        age: record.age,
        bodyFat: record.bodyFat,
        skeletalMuscleMass: record.skeletalMuscleMass,
        bmr: record.bmr,
        visceralFatLevel: record.visceralFatLevel,
        bodyWater: record.bodyWater,
      })),
    })
  }

  const mealPlans: Array<Promise<unknown>> = []

  if (payload.dailyMeals) {
    mealPlans.push(
      prisma.mealPlan.upsert({
        where: {
          id: `daily_${userId}_${payload.dailyMeals.date}`,
        },
        update: {
          payload: asJson(payload.dailyMeals),
          targetCalories: payload.dailyMeals.targetCalories,
          targetProtein: payload.dailyMeals.targetProtein,
          promptVersion: payload.promptVersion ?? 'v2-regional',
          preferredCuisine: profile?.preferredCuisine ?? null,
          source: 'local-migration',
          planType: 'daily',
          planDate: payload.dailyMeals.date,
        },
        create: {
          id: `daily_${userId}_${payload.dailyMeals.date}`,
          userId,
          planType: 'daily',
          planDate: payload.dailyMeals.date,
          payload: asJson(payload.dailyMeals),
          targetCalories: payload.dailyMeals.targetCalories,
          targetProtein: payload.dailyMeals.targetProtein,
          promptVersion: payload.promptVersion ?? 'v2-regional',
          preferredCuisine: profile?.preferredCuisine ?? null,
          source: 'local-migration',
        },
      })
    )
  }

  if (payload.weeklyPlan) {
    mealPlans.push(
      prisma.mealPlan.upsert({
        where: { id: payload.weeklyPlan.id },
        update: {
          payload: asJson(payload.weeklyPlan),
          targetCalories: payload.weeklyPlan.targetCalories,
          targetProtein: payload.weeklyPlan.targetProtein,
          promptVersion: payload.promptVersion ?? 'v2-regional',
          preferredCuisine: profile?.preferredCuisine ?? null,
          source: 'local-migration',
          planType: 'weekly',
          weekStart: payload.weeklyPlan.days[0]?.date ?? null,
        },
        create: {
          id: payload.weeklyPlan.id,
          userId,
          planType: 'weekly',
          weekStart: payload.weeklyPlan.days[0]?.date ?? null,
          payload: asJson(payload.weeklyPlan),
          targetCalories: payload.weeklyPlan.targetCalories,
          targetProtein: payload.weeklyPlan.targetProtein,
          promptVersion: payload.promptVersion ?? 'v2-regional',
          preferredCuisine: profile?.preferredCuisine ?? null,
          source: 'local-migration',
        },
      })
    )
  }

  await Promise.all(mealPlans)
}

export async function getUserCloudSnapshot(userId: string): Promise<CloudSnapshot | null> {
  const user = await prisma.user.findUnique({
    where: { id: userId },
    include: {
      inbodyHistory: { orderBy: { createdAt: 'asc' } },
      mealPlans: {
        where: { OR: [{ planType: 'daily' }, { planType: 'weekly' }] },
        orderBy: { createdAt: 'desc' },
      },
    },
  })

  if (!user) return null

  const latestDaily = user.mealPlans.find((plan) => plan.planType === 'daily')?.payload as DailyMeals | undefined
  const latestWeekly = user.mealPlans.find((plan) => plan.planType === 'weekly')?.payload as WeeklyPlan | undefined
  const localState = (user.localState ?? {}) as Partial<MigrationPayload['appState']>

  return {
    profile: {
      goal: (user.goal as UserProfile['goal']) ?? 'maintain',
      activityLevel: 'moderate',
      fitnessLevel: 'beginner',
      dietaryRestrictions: (user.dietaryRestrictions as string[]) ?? [],
      proteinPreferences: [],
      carbPreferences: [],
      cuisinePreferences: [],
      preferredCuisine: (user.preferredCuisine as UserProfile['preferredCuisine']) ?? undefined,
      dislikedIngredients: (user.dislikedIngredients as string[]) ?? [],
      cookingStyle: 'both',
      isPro: user.isPro,
      isAdFree: user.isAdFree,
      autoGenerateDaily: user.autoGenerateDaily,
    },
    inbodyHistory: user.inbodyHistory.map((record) => ({
      id: record.id,
      date: record.entryDate,
      weight: record.weight,
      height: record.height,
      gender: record.gender as 'male' | 'female',
      age: record.age,
      bodyFat: record.bodyFat ?? undefined,
      skeletalMuscleMass: record.skeletalMuscleMass ?? undefined,
      bmr: record.bmr ?? undefined,
      visceralFatLevel: record.visceralFatLevel ?? undefined,
      bodyWater: record.bodyWater ?? undefined,
    })),
    dailyMeals: latestDaily ?? null,
    weeklyPlan: latestWeekly ?? null,
    currentStreak: user.streak,
    appState: {
      trainingHistory: localState?.trainingHistory ?? [],
      favorites: localState?.favorites ?? [],
      macroScore: localState?.macroScore ?? 0,
      unlockedParts: localState?.unlockedParts ?? ['head_none', 'top_basic_white', 'bottom_sweats_gray', 'acc_none'],
      equippedLoadout: localState?.equippedLoadout ?? {
        head: 'head_none',
        top: 'top_basic_white',
        bottom: 'bottom_sweats_gray',
        accessory: 'acc_none',
      },
      lang: localState?.lang === 'en' ? 'en' : 'zh',
    },
  }
}

